import os

def replace_tab_with_spaces_or_tabs(file_path):
    print("""Реализовать функциональность которая бы сворачивала и разворачивалась символы табуляции в файлe/
то есть на вход передается файл необходимо заменить все символы табуляции на 4 пробела либо же заменить все комбинации из 4 символов пробела на символ табуляции в зависимости от опций указанной пользователем
перепиши это задлание красиво в код""")
    if not os.path.isfile(file_path):
        print("Файл не существует.")
        return

    # Запрашиваем у пользователя выбор опции
    option = input("Выберите опцию (1 - замена символов табуляции на 4 пробела, 2 - замена 4 пробелов на символ табуляции): ")

    # Открываем файл для чтения и записи
    with open(file_path, 'r+') as file:
        content = file.read()

        # Заменяем символы табуляции на 4 пробела
        if option == '1':
            content = content.replace('\t', ' ' * 4)
        # Заменяем комбинации из 4 пробелов на символ табуляции
        elif option == '2':
            content = content.replace(' ' * 4, '\t')
        else:
            print("Некорректный выбор опции.")
            return

        # Возвращаемся в начало файла, очищаем его и записываем измененный контент
        file.seek(0)
        file.write(content)
        file.truncate()

# Путь к файлу, который нужно обработать
file_path = 'text.txt'

# Вызываем функцию для замены символов табуляции
replace_tab_with_spaces_or_tabs(file_path)